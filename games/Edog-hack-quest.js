// ---------- ZOMBIE SPAWN ----------
function spawnZombiesOnce() {
  if (zombiesSpawned) return;
  const w = width();
  const h = height();
  addSprite(1, 1, zombie);
  addSprite(w - 2, 1, zombie);
  addSprite(1, h - 2, zombie);
  zombiesSpawned = true;
}

