function followPlayer() {
  const p = getFirst(edog);
  const g = getFirst(egirl);
  if (!p || !g) return;
  const dx = p.x - g.x;
  const dy = p.y - g.y;
  if (Math.abs(dx) + Math.abs(dy) > 1) {
    if (Math.abs(dx) > Math.abs(dy)) g.x += Math.sign(dx);
    else g.y += Math.sign(dy);
  }
}

