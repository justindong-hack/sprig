onInput("w", () => { const e = getFirst(edog); if (e) e.y--; });
onInput("a", () => { const e = getFirst(edog); if (e) e.x--; });
onInput("s", () => { const e = getFirst(edog); if (e) e.y++; });
onInput("d", () => { const e = getFirst(edog); if (e) e.x++; });

// ---------- ATTACK ----------
onInput("j", () => attack(1));
onInput("l", () => attack(2));

function attack(range) {
  const e = getFirst(edog);
  if (!e) return;
  if (hasSword) range++;
  //Glow effect when sword is used
  const swordSprite = getFirst(swordGlow);
  if(swordSprite) {
    swordSprite.x = e.x; //Position the glow to follow the sword
    swordSprite.x = e.y;
    swordSprite.addEffect("blink", { speed: 5}); 
  }
  // Existing attack logic
  getAll(zombie).forEach(z => {
    if (Math.abs(z.x - e.x) <= range && Math.abs(z.y - e.y) <= range) {
      // Simple shake effect by moving the zombie
    let shakeCount = 0;
    const shakeInterval = setInterval(() => {
      if (shakeCount < 5) {
        z.x += (shakeCount % 2 === 0 ? 5 : -5);  // Move left/right
        shakeCount++;
      } else {
        clearInterval(shakeInterval);
        z.remove();  // Remove zombie after shake
      }
    }, 100);  // Shake every 100ms
  }
});

  const b = getFirst(boss);
  if (b && Math.abs(b.x - e.x) <= range && Math.abs(b.y - e.y) <= range) {
    bossHP--;
    if (bossHP <= 0) {
      b.remove();
      clearText();
      addText("BOSS DEFEATED!", { x: 3, y: 6 });
    }
  }
}

// ---------- ZOMBIE AI ----------
setInterval(() => {
  const e = getFirst(edog);
  if (!e) return;
  getAll(zombie).forEach(z => {
    if (z.x < e.x) z.x++;
    else if (z.x > e.x) z.x--;
    if (z.y < e.y) z.y++;
    else if (z.y > e.y) z.y--;
  });
}, 500);

// ---------- BOSS AI ----------
